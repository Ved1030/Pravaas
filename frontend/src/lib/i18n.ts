export function createI18n() {
  const listeners = new Set<() => void>();

  function getLanguage(): Language {
    if (typeof window === 'undefined') return 'en';
    return (localStorage.getItem(STORAGE_KEY) as Language) || 'en';
  }

  function setLanguage(lang: Language) {
    localStorage.setItem(STORAGE_KEY, lang);
    listeners.forEach((fn) => fn());
  }

  function t(key: TranslationKey, lang?: Language): string {
    const currentLang = lang || getLanguage();
    const langTranslations = translations[currentLang];
    return langTranslations?.[key] || enTranslations[key] || key;
  }

  function subscribe(fn: () => void) {
    listeners.add(fn);
    return () => listeners.delete(fn);
  }

  return { t, setLanguage, getLanguage, subscribe };
}

export const { t, setLanguage, getLanguage, subscribe } = createI18n();

export function useLanguage() {
  const [lang, setLang] = useState<Language>(getLanguage);

  useEffect(() => {
    const unsub = subscribe(() => setLang(getLanguage()));
    return unsub;
  }, []);

  const changeLanguage = useCallback((newLang: Language) => {
    setLanguage(newLang);
  }, []);

  return { language: lang, setLanguage: changeLanguage, t };
}
